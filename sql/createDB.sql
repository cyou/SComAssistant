  `WATERPERCENTFLG`  INT DEFAULT 0 COMMENT '地面含水量(0：有效 1：无效)',
  PRIMARY KEY (`READTIME`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;

